@app.command()
def gateway(
    port: int = typer.Option(18790, "--port", "-p", help="Gateway port"),
    verbose: bool = typer.Option(False, "--verbose", "-v", help="Verbose output"),
):
    """
    启动 coffiebot 网关 (多渠道服务)。

    【重要】这里是系统初始化的入口点，但 Skills 加载不在这里。
    Skills 的加载发生在：
    - AgentLoop 创建时（__init__）实例化 ContextBuilder
    - 每条消息处理时调用 build_messages()（在 _process_message 中）

    初始化流程：
    1. 加载配置 → provider, workspace, cron, etc.
    2. 创建 AgentLoop（包含 ContextBuilder，但 skills 尚未加载）
    3. 创建 ChannelManager（多渠道适配器）
    4. 创建 CronService（计划任务）
    5. 创建 HeartbeatService（心跳任务）
    6. 启动事件循环

    Skills 是"懒加载"的：每条消息到达时，在 _process_message() 中调用
    build_messages() 才会真正组装 skills 进系统提示。
    """
    from coffiebot.config.loader import load_config, get_data_dir
    from coffiebot.bus.queue import MessageBus
    from coffiebot.agent.loop import AgentLoop
    from coffiebot.channels.manager import ChannelManager
    from coffiebot.session.manager import SessionManager
    from coffiebot.cron.service import CronService
    from coffiebot.cron.types import CronJob
    from coffiebot.heartbeat.service import HeartbeatService

    # 配置 loguru 日志输出：同时输出到 stdout 和日志文件
    from loguru import logger

    # 获取日志目录
    logs_dir = Path.home() / ".coffiebot" / "logs"
    logs_dir.mkdir(parents=True, exist_ok=True)

    # 配置日志文件输出（rotation: 每天轮转，保留 7 天）
    logger.add(
        str(logs_dir / "coffiebot.log"),
        format="{time:YYYY-MM-DD HH:mm:ss} | {level: <8} | {name}:{function}:{line} - {message}",
        rotation="00:00",  # 每天午夜轮转
        retention="7 days",  # 保留 7 天的日志
        level="DEBUG",
    )

    if verbose:
        import logging
        logging.basicConfig(level=logging.DEBUG)

    console.print(f"{__logo__} Starting coffiebot gateway on port {port}...")

    # 【初始化第一阶段】加载配置和基础服务
    config = load_config()
    sync_workspace_templates(config.workspace_path)
    bus = MessageBus()
    provider = _make_provider(config)
    session_manager = SessionManager(config.workspace_path)

    # ========================================================================
    # 【CRON 初始化】第一步：创建 CronService 实例
    # ========================================================================
    # 说明：
    #   - get_data_dir() 返回 ~/.coffiebot（或 /srv/coffiebot/data）
    #   - cron_store_path 指向本地/远程的 jobs.json 文件
    #   - CronService 会在后续的 start() 中从这个文件读取所有 cron job 定义
    #   - 但此时还没有启动定时器，只是实例化对象
    # Create cron service first (callback set after agent creation)
    cron_store_path = get_data_dir() / "cron" / "jobs.json"
    cron = CronService(cron_store_path)

    # 【初始化第二阶段】创建 AgentLoop
    # 此时 ContextBuilder 被实例化，SkillsLoader 也被初始化
    # 但 skills 内容还未加载到任何系统提示中（还没有消息处理）
    agent = AgentLoop(
        bus=bus,
        provider=provider,
        workspace=config.workspace_path,
        model=config.agents.defaults.model,
        temperature=config.agents.defaults.temperature,
        max_tokens=config.agents.defaults.max_tokens,
        max_iterations=config.agents.defaults.max_tool_iterations,
        memory_window=config.agents.defaults.memory_window,
        brave_api_key=config.tools.web.search.api_key or None,
        exec_config=config.tools.exec,
        cron_service=cron,  # 将 cron 实例传给 agent
        restrict_to_workspace=config.tools.restrict_to_workspace,
        session_manager=session_manager,
        mcp_servers=config.tools.mcp_servers,
        channels_config=config.channels,
        openviking_config=config.openviking,
    )

    # ========================================================================
    # 【CRON 初始化】第二步：为 CronService 注册回调函数
    # ========================================================================
    # 说明：
    #   - CronService 本身只负责定时和触发，具体执行什么由外部决定
    #   - 我们通过 cron.on_job = on_cron_job 给它注册一个回调函数
    #   - 当定时器触发时，CronService 会调用这个回调函数处理任务
    #   - 回调函数必须是异步的（async def），因为 CronService._execute_job() 是 async
    #
    # 这个设计是"依赖注入"模式：
    #   - CronService 不依赖 Agent，职责单一（只管定时）
    #   - gateway() 函数负责将两者联系起来
    #
    # Set cron callback (needs agent)
    async def on_cron_job(job: CronJob) -> str | None:
        """
        当 cron 任务触发时，这个函数会被 CronService._execute_job() 调用。

        执行流程：
          1. job 是 CronJob 对象，包含了 payload（消息内容）和配置
          2. 调用 agent.process_direct() 处理消息（这会触发 LLM）
          3. 如果配置了 deliver=true，将结果投递到目标渠道（飞书等）

        参数：
          - job.payload.message: 要发给 LLM 的 prompt，如 "你是一名 AI 资讯编辑..."
          - job.payload.channel: 目标渠道，如 "feishu"
          - job.payload.to: 目标 ID，如飞书群组 ID
          - job.payload.deliver: 是否投递，true 则发送到目标渠道

        返回值：
          - LLM 的最终响应文本
        """
        # 【步骤 A】调用 agent 处理这个任务
        # session_key 用于隔离不同 cron job 的会话历史
        # 例如，早报和晚报会有不同的 session，历史消息不混淆
        response = await agent.process_direct(
            job.payload.message,                    # 早报的 prompt
            session_key=f"cron:{job.id}",          # 例如 "cron:ai-news-morning"
            channel=job.payload.channel or "cli",   # "feishu"
            chat_id=job.payload.to or "direct",    # 飞书群组 ID
        )

        # 【步骤 B】投递结果到目标渠道
        # 如果配置了投递，则通过 MessageBus 发送到飞书
        # MessageBus 是一个异步队列，ChannelManager 会从中消费消息
        if job.payload.deliver and job.payload.to:
            from coffiebot.bus.events import OutboundMessage
            # 构建 metadata，若配置了 card_template 则传递给渠道侧做模板渲染
            metadata: dict[str, Any] = {}
            if job.payload.card_template:
                metadata["card_template"] = job.payload.card_template
            await bus.publish_outbound(OutboundMessage(
                channel=job.payload.channel or "cli",  # 渠道：飞书
                chat_id=job.payload.to,                # 群组 ID
                content=response or "",                # AI 早报的内容
                metadata=metadata,
            ))

        return response

    # 【关键】注册回调：将函数赋值给 cron 实例
    # 后续当 CronService._on_timer() 触发时，会调用 self.on_job(job)
    # 即调用这个 on_cron_job() 函数
    cron.on_job = on_cron_job

    # 【初始化第三阶段】创建渠道管理器和心跳服务
    channels = ChannelManager(config, bus)

    def _pick_heartbeat_target() -> tuple[str, str]:
        """Pick a routable channel/chat target for heartbeat-triggered messages."""
        enabled = set(channels.enabled_channels)
        # Prefer the most recently updated non-internal session on an enabled channel.
        for item in session_manager.list_sessions():
            key = item.get("key") or ""
            if ":" not in key:
                continue
            channel, chat_id = key.split(":", 1)
            if channel in {"cli", "system"}:
                continue
            if channel in enabled and chat_id:
                return channel, chat_id
        # Fallback keeps prior behavior but remains explicit.
        return "cli", "direct"

    # Create heartbeat service
    # 【Skills 将在这里被加载】当心跳任务执行时，也会触发 build_messages() → 【skills 加载点】
    async def on_heartbeat_execute(tasks: str) -> str:
        """Phase 2: execute heartbeat tasks through the full agent loop."""
        channel, chat_id = _pick_heartbeat_target()

        async def _silent(*_args, **_kwargs):
            pass

        return await agent.process_direct(
            tasks,
            session_key="heartbeat",
            channel=channel,
            chat_id=chat_id,
            on_progress=_silent,
        )

    async def on_heartbeat_notify(response: str) -> None:
        """Deliver a heartbeat response to the user's channel."""
        from coffiebot.bus.events import OutboundMessage
        channel, chat_id = _pick_heartbeat_target()
        if channel == "cli":
            return  # No external channel available to deliver to
        await bus.publish_outbound(OutboundMessage(channel=channel, chat_id=chat_id, content=response))

    hb_cfg = config.gateway.heartbeat
    heartbeat = HeartbeatService(
        workspace=config.workspace_path,
        provider=provider,
        model=agent.model,
        on_execute=on_heartbeat_execute,
        on_notify=on_heartbeat_notify,
        interval_s=hb_cfg.interval_s,
        enabled=hb_cfg.enabled,
    )
    
    if channels.enabled_channels:
        console.print(f"[green]✓[/green] Channels enabled: {', '.join(channels.enabled_channels)}")
    else:
        console.print("[yellow]Warning: No channels enabled[/yellow]")

    cron_status = cron.status()
    if cron_status["jobs"] > 0:
        console.print(f"[green]✓[/green] Cron: {cron_status['jobs']} scheduled jobs")

    console.print(f"[green]✓[/green] Heartbeat: every {hb_cfg.interval_s}s")

    # ========================================================================
    # 【CRON 初始化】第三步：启动事件循环
    # ========================================================================
    async def run():
        """
        主事件循环。启动所有服务并并发运行。

        执行顺序：
          1. cron.start() - 加载 jobs.json，启动定时器
          2. heartbeat.start() - 启动心跳任务
          3. asyncio.gather() - 并发运行以下服务：
             a) agent.run() - Agent 消息循环（消费来自 MessageBus 的消息）
             b) channels.start_all() - 多渠道服务（飞书、Telegram 等）

        关键点：
          - asyncio.gather() 使这些服务并发运行，互不阻塞
          - cron 服务会在后台运行定时器，到点时调用 on_cron_job 回调
          - 如果任何服务抛出异常，整个循环停止
          - KeyboardInterrupt (Ctrl+C) 会优雅地关闭所有服务
        """
        try:
            # ──────────────────────────────────────────────────────────────
            # 【CRON 启动】await cron.start()
            # ──────────────────────────────────────────────────────────────
            # 这是 Cron 服务启动的关键一行。具体做什么？
            #
            # 在 coffiebot/cron/service.py 中的 CronService.start()：
            #   1. self._load_store()
            #      - 从 jobs.json 读取所有 cron job 定义
            #      - 解析为 CronJob 对象列表
            #      - 示例：加载 ai-news-morning 和 ai-news-evening 两个任务
            #
            #   2. self._recompute_next_runs()
            #      - 计算每个 job 的下次执行时间
            #      - 使用 croniter 库解析 cron 表达式 "30 10 * * *"
            #      - 例如，假设现在是 2026-03-01 09:00，则下次执行是 10:30
            #      - 存入 job.state.next_run_at_ms
            #
            #   3. self._arm_timer()
            #      - 找出最近的任务触发时间
            #      - 创建 asyncio.sleep() 等待到该时间
            #      - 当时间到达时，调用 self._on_timer()
            #
            # 之后的行为（不在这一行之内，但会在后续发生）：
            #   - asyncio.sleep() 在后台运行，不阻塞其他任务
            #   - 定时器每次触发都会调用 on_cron_job 回调
            #   - 执行完后，_arm_timer() 会自动为下一个任务计时
            await cron.start()

            # 心跳任务启动（与 cron 类似，定期执行一些背景任务）
            await heartbeat.start()

            # 并发运行 agent 和所有渠道
            await asyncio.gather(
                agent.run(),           # Agent 的消息循环
                channels.start_all(),  # 多渠道服务（飞书、Telegram...）
            )

        except KeyboardInterrupt:
            # 用户按 Ctrl+C 时触发
            console.print("\nShutting down...")

        finally:
            # 无论正常退出还是异常，都要清理资源
            await agent.close_mcp()
            heartbeat.stop()
            cron.stop()              # 停止定时器
            agent.stop()
            await channels.stop_all()

    # ────────────────────────────────────────────────────────────────────
    # 【最后一步】启动整个应用
    # ────────────────────────────────────────────────────────────────────
    # asyncio.run() 是 Python 3.7+ 的标准方式，启动异步事件循环
    # 它会运行 run() 函数直到其完成（通常是永远，直到 Ctrl+C）
    asyncio.run(run())




